    private void GoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GoActionPerformed
        // TODO add your handling code here:
        int min = Integer.parseInt(jTextField8.getText());
        int max = Integer.parseInt(jTextField9.getText());
        
        if(max < min ){
            JOptionPane.showMessageDialog(null, "Max can not be less than Min", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        List<Car> res = new ArrayList<Car>();
        for(Car c: carCatalog.getCarcatalog()){
            if(min <= c.getSeats() && c.getSeats() <= max){
                res.add(c);
            }
        }
        
        refreshTable(res);
        
    }//GEN-LAST:event_GoActionPerformed

