    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = carTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Car c = (Car)carTable.getValueAt(row,0);
        
        jTextField1.setText(c.getMake());
        jTextField2.setText(c.getModelNumber());
        jTextField3.setText(c.getSerialNumber());
        jTextField4.setText(String.valueOf(c.getYear()));
        jTextField5.setText(String.valueOf(c.getSeats()));
        jTextField6.setText(c.getCity());
        
         jCheckBox1.setSelected(c.isAvailabile());
        jCheckBox2.setSelected(c.isMaintainceCertificate());
        
    }//GEN-LAST:event_jButton2ActionPerformed

