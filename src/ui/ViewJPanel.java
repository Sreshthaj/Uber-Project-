    public void refreshTable(List<Car> carCatalog){
        int rowCount = carTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel) carTable.getModel();
        for(int i=rowCount - 1;i>=0;i--){
            model.removeRow(i);
        }
        for(Car c: carCatalog){
            Object row[] = new Object[8];
            row[0] = c;
            row[1] = c.getModelNumber();
            row[2] = c.getSerialNumber();
            row[3] = c.getYear();
            row[4] = c.getSeats();
            row[5] = c.getCity();
            row[6] = c.isAvailabile();
            row[7] = c.isMaintainceCertificate();
            model.addRow(row);
        }
    }
    
    
