    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = carTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
         Car c = (Car)carTable.getValueAt(row,0);
         
         c.setSerialNumber("dummy");
         
        if (jTextField6.getText() == "" || jTextField6.getText().equals("")){
            JOptionPane.showMessageDialog(null, "City cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jTextField1.getText() == "" || jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Model cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jTextField2.getText() == "" || jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Model number cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jTextField5.getText() == "" || jTextField5.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Seats cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jTextField3.getText() == "" || jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Serial Number cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jTextField4.getText() == "" || jTextField4.getText().equals("")){
            JOptionPane.showMessageDialog(null, "year cannot be empty!!", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
           for(Car k: carCatalog.getCarcatalog()){
            if(k.getSerialNumber() == jTextField3.getText() || k.getSerialNumber().equals(jTextField3.getText())){
                JOptionPane.showMessageDialog(null, "Serial Number not unique!!", "Info", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
       
        
        c.setMake(jTextField1.getText());
        c.setModelNumber(jTextField2.getText());
        c.setSerialNumber(jTextField3.getText());
        c.setYear(Integer.parseInt(jTextField4.getText()));
        c.setSeats(Integer.parseInt(jTextField5.getText()));
        c.setCity(jTextField6.getText());
        c.setAvailabile(jCheckBox1.isSelected());
        c.setMaintainceCertificate(jCheckBox2.isSelected());
        
        refreshTable(carCatalog.getCarcatalog());
    }//GEN-LAST:event_jButton3ActionPerformed

